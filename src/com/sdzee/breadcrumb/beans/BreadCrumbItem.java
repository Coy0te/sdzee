package com.sdzee.breadcrumb.beans;

public class BreadCrumbItem {
    private String titre;
    private String url;

    public String getUrl() {
        return url;
    }

    public void setUrl( String url ) {
        this.url = url;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre( String titre ) {
        this.titre = titre;
    }
}
